--------------------------------------------------------------------------------
-- TABLE: temp_influencer_data
--  - AI로 추출된 인플루언서 임시 저장 (influencer_id 매핑 전)
--------------------------------------------------------------------------------
CREATE TABLE public.temp_influencer_data (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,  -- 숫자 PK

  -- 추출된 인플루언서 정보
  influencer_name VARCHAR(255) NOT NULL,
  
  -- 소스 정보
  source_type VARCHAR(50)   NOT NULL,
  source_id   VARCHAR(1023) NOT NULL,

  -- 매핑 정보
  influencer_id BIGINT NULL,
  
  -- 관리 정보
  admin_message VARCHAR(1023) NULL,
  is_processed  BOOLEAN NOT NULL DEFAULT FALSE,
  processed_at  TIMESTAMPTZ NULL,
  
  -- 시스템 정보
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ          DEFAULT CURRENT_TIMESTAMP,

  -- 외래키 (influencers.id 를 참조)
  CONSTRAINT temp_influencer_data_influencer_id_fkey
    FOREIGN KEY (influencer_id)
    REFERENCES public.influencers (id)
    ON UPDATE CASCADE ON DELETE SET NULL,

  -- 제약조건
  CONSTRAINT temp_influencer_data_source_type_check 
    CHECK (source_type IN ('youtube_video', 'instagram_post', 'blog_post', 'text')),
  
  CONSTRAINT temp_influencer_data_influencer_name_not_empty_check 
    CHECK (LENGTH(TRIM(influencer_name)) > 0),
  
  -- 중복 방지: 같은 소스에서 같은 인플루언서명은 한 번만
  CONSTRAINT temp_influencer_data_unique_source_influencer
    UNIQUE (source_type, source_id, influencer_name)
) TABLESPACE pg_default;

