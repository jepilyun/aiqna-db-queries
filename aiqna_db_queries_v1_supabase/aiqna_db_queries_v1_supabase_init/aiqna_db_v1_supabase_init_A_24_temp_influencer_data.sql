-- 추출된 인플루언서명 저장
CREATE OR REPLACE FUNCTION public.add_temp_influencer_data(
    p_influencer_name VARCHAR(255),
    p_source_type     VARCHAR(50),
    p_source_id       VARCHAR(1023)
)
RETURNS BIGINT
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
DECLARE
    v_id BIGINT;
BEGIN
    INSERT INTO public.temp_influencer_data (
        influencer_name, source_type, source_id
    ) VALUES (
        TRIM(p_influencer_name), p_source_type, p_source_id
    )
    ON CONFLICT (source_type, source_id, influencer_name) DO NOTHING
    RETURNING id INTO v_id;

    RETURN v_id;
END;
$$;

-- influencer_id 매핑 및 자동 처리
CREATE OR REPLACE FUNCTION public.map_temp_influencer_data_to_influencer_id(
    p_temp_influencer_data_id BIGINT,
    p_influencer_id BIGINT,
    p_admin_message VARCHAR(1023) DEFAULT NULL
)
RETURNS VOID
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
BEGIN
    UPDATE public.temp_influencer_data
    SET influencer_id = p_influencer_id,
        admin_message = p_admin_message,
        is_processed  = TRUE,
        processed_at  = CURRENT_TIMESTAMP,
        updated_at    = CURRENT_TIMESTAMP
    WHERE id = p_temp_influencer_data_id;
END;
$$;

-- 미처리 인플루언서명 목록 조회 (관리자용)
CREATE OR REPLACE FUNCTION public.get_unprocessed_temp_influencer_data(
    p_limit INTEGER DEFAULT 100
)
RETURNS TABLE (
    influencer_name VARCHAR(255),
    source_type     VARCHAR(50),
    source_id       VARCHAR(1023),
    occurrence_count BIGINT,
    first_seen       TIMESTAMPTZ,
    last_seen        TIMESTAMPTZ
)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
    SELECT 
        influencer_name,
        source_type,
        source_id,
        COUNT(*)        AS occurrence_count,
        MIN(created_at) AS first_seen,
        MAX(created_at) AS last_seen
    FROM public.temp_influencer_data
    WHERE is_processed = FALSE
    GROUP BY influencer_name, source_type, source_id
    ORDER BY occurrence_count DESC, last_seen DESC
    LIMIT p_limit;
$$;

-- 인플루언서명 검색 (유사 매칭)
CREATE OR REPLACE FUNCTION public.search_similar_temp_influencer_names(
    p_influencer_name VARCHAR(255),
    p_limit INTEGER DEFAULT 10
)
RETURNS TABLE (
    influencer_name  VARCHAR(255),
    influencer_id    BIGINT,
    occurrence_count BIGINT,
    is_processed     BOOLEAN,
    similarity_score REAL
)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
    SELECT 
        ti.influencer_name,
        ti.influencer_id,
        COUNT(*) AS occurrence_count,
        ti.is_processed,
        similarity(ti.influencer_name, p_influencer_name) AS similarity_score
    FROM public.temp_influencer_data ti
    WHERE ti.influencer_name % p_influencer_name
    GROUP BY ti.influencer_name, ti.influencer_id, ti.is_processed
    ORDER BY similarity_score DESC, occurrence_count DESC
    LIMIT p_limit;
$$;

-- 인플루언서명 일괄 매핑 (같은 이름을 한 번에)
CREATE OR REPLACE FUNCTION public.batch_map_temp_influencer_name_to_id(
    p_influencer_name VARCHAR(255),
    p_influencer_id BIGINT,
    p_admin_message VARCHAR(1023) DEFAULT NULL
)
RETURNS INTEGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
DECLARE
    v_updated_count INTEGER;
BEGIN
    UPDATE public.temp_influencer_data
    SET influencer_id = p_influencer_id,
        admin_message = p_admin_message,
        is_processed  = TRUE,
        processed_at  = CURRENT_TIMESTAMP,
        updated_at    = CURRENT_TIMESTAMP
    WHERE influencer_name = p_influencer_name
      AND is_processed = FALSE;

    GET DIAGNOSTICS v_updated_count = ROW_COUNT;
    RETURN v_updated_count;
END;
$$;

-- 통계 조회
CREATE OR REPLACE FUNCTION public.get_temp_influencer_data_statistics()
RETURNS TABLE (
    total_count BIGINT,
    processed_count BIGINT,
    unprocessed_count BIGINT,
    unique_influencer_names BIGINT,
    mapped_influencer_ids BIGINT
)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
    SELECT 
        COUNT(*) AS total_count,
        COUNT(*) FILTER (WHERE is_processed = TRUE)  AS processed_count,
        COUNT(*) FILTER (WHERE is_processed = FALSE) AS unprocessed_count,
        COUNT(DISTINCT influencer_name) AS unique_influencer_names,
        COUNT(DISTINCT influencer_id) FILTER (WHERE influencer_id IS NOT NULL) AS mapped_influencer_ids
    FROM public.temp_influencer_data;
$$;
