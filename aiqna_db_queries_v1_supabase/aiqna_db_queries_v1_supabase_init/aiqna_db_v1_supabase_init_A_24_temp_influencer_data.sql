-- updated_at 자동 업데이트
CREATE TRIGGER trigger_update_temp_influencer_data_updated_at
  BEFORE UPDATE ON public.temp_influencer_data
  FOR EACH ROW
  EXECUTE FUNCTION update_updated_at_column();

-- influencer_id가 설정되면 자동으로 is_processed = TRUE로 변경
CREATE OR REPLACE FUNCTION auto_mark_processed_on_temp_influencer_id()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    IF NEW.influencer_id IS NOT NULL AND OLD.influencer_id IS NULL THEN
        NEW.is_processed := TRUE;
        NEW.processed_at := CURRENT_TIMESTAMP;
    END IF;
    RETURN NEW;
END;
$$;

CREATE TRIGGER trigger_auto_mark_processed_temp_influencer
  BEFORE UPDATE ON public.temp_influencer_data
  FOR EACH ROW
  WHEN (NEW.influencer_id IS NOT NULL AND OLD.influencer_id IS NULL)
  EXECUTE FUNCTION auto_mark_processed_on_temp_influencer_id();

