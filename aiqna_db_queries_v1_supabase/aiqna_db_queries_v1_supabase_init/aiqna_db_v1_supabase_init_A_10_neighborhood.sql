--------------------------------------------------------------------------------
-- TABLE: neighborhoods
--------------------------------------------------------------------------------
CREATE TABLE public.neighborhoods (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,  -- ← 숫자 PK
  country_code    VARCHAR(2)    NULL,
  city_code       VARCHAR(96)   NULL,

  name_en         VARCHAR(100)  NOT NULL,
  name_native     VARCHAR(100)  NULL,
  name_ko         VARCHAR(100)  NULL,
  order_num       SMALLINT      NOT NULL DEFAULT 0,

  description_en      VARCHAR(1023) NULL,
  description_native  VARCHAR(1023) NULL,

  google_place_id VARCHAR(63)   NULL,
  youtube_ch_id   VARCHAR(48)   NULL,
  instagram_id    VARCHAR(48)   NULL,

  -- 위도/경도
  latitude   NUMERIC(10, 8) NULL,
  longitude  NUMERIC(11, 8) NULL,

  -- 자동 생성되는 지리 객체 컬럼
  location GEOGRAPHY(POINT, 4326)
    GENERATED ALWAYS AS (
      CASE 
        WHEN latitude IS NOT NULL AND longitude IS NOT NULL 
        THEN geography(ST_SetSRID(ST_MakePoint(longitude, latitude), 4326))
        ELSE NULL
      END
    ) STORED,

  radius_m        INTEGER       NULL,
  address_en      VARCHAR(255)  NULL,
  address_native  VARCHAR(255)  NULL,
  google_map_url  VARCHAR(255)  NULL,
  naver_map_url   VARCHAR(255)  NULL,

  created_at   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  created_by   VARCHAR(511) NULL,
  updated_at   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  is_active    BOOLEAN     NOT NULL DEFAULT TRUE,
  deactivated_at TIMESTAMPTZ NULL,
  deactivated_by VARCHAR(511) NULL,
  is_display   BOOLEAN     NOT NULL DEFAULT TRUE,

  thumbnail_main_1 VARCHAR(1023) NULL,
  thumbnail_main_2 VARCHAR(1023) NULL,
  thumbnail_main_3 VARCHAR(1023) NULL,
  thumbnail_main_4 VARCHAR(1023) NULL,
  thumbnail_main_5 VARCHAR(1023) NULL,

  thumbnail_1      VARCHAR(1023) NULL,
  thumbnail_2      VARCHAR(1023) NULL,
  thumbnail_3      VARCHAR(1023) NULL,
  thumbnail_4      VARCHAR(1023) NULL,
  thumbnail_5      VARCHAR(1023) NULL,

  thumbnail_vertical_1 VARCHAR(1023) NULL,
  thumbnail_vertical_2 VARCHAR(1023) NULL,
  thumbnail_vertical_3 VARCHAR(1023) NULL,
  thumbnail_vertical_4 VARCHAR(1023) NULL,
  thumbnail_vertical_5 VARCHAR(1023) NULL,

  -- FK
  CONSTRAINT neighborhoods_country_code_fkey
    FOREIGN KEY (country_code)
    REFERENCES public.countries (country_code)
    ON UPDATE CASCADE ON DELETE CASCADE,

  CONSTRAINT neighborhoods_city_code_fkey
    FOREIGN KEY (city_code)
    REFERENCES public.cities (city_code)
    ON UPDATE CASCADE ON DELETE CASCADE,

  -- 값 검증
  CONSTRAINT neighborhoods_radius_m_check CHECK (radius_m IS NULL OR radius_m >= 0),
  CONSTRAINT neighborhoods_latitude_check CHECK (latitude IS NULL OR (latitude BETWEEN -90 AND 90)),
  CONSTRAINT neighborhoods_longitude_check CHECK (longitude IS NULL OR (longitude BETWEEN -180 AND 180)),
  CONSTRAINT neighborhoods_order_num_check CHECK (order_num >= 0)
) TABLESPACE pg_default;

-- INDEXES
CREATE INDEX IF NOT EXISTS neighborhoods_city_order_idx
  ON public.neighborhoods (city_code, order_num, id);

CREATE INDEX IF NOT EXISTS neighborhoods_country_idx
  ON public.neighborhoods (country_code);

CREATE INDEX IF NOT EXISTS neighborhoods_city_active_display_idx
  ON public.neighborhoods (city_code, is_active, is_display)
  WHERE is_active = TRUE AND is_display = TRUE;

CREATE INDEX IF NOT EXISTS neighborhoods_is_active_idx
  ON public.neighborhoods (is_active, order_num)
  WHERE is_active = TRUE;

CREATE INDEX IF NOT EXISTS neighborhoods_location_gist_idx
  ON public.neighborhoods USING GIST (location)
  WHERE location IS NOT NULL;

CREATE INDEX IF NOT EXISTS neighborhoods_lat_lng_idx
  ON public.neighborhoods (latitude, longitude)
  WHERE latitude IS NOT NULL AND longitude IS NOT NULL;

CREATE INDEX IF NOT EXISTS neighborhoods_name_en_trgm_idx
  ON public.neighborhoods USING gin (name_en gin_trgm_ops);

CREATE INDEX IF NOT EXISTS neighborhoods_name_ko_trgm_idx
  ON public.neighborhoods USING gin (name_ko gin_trgm_ops)
  WHERE name_ko IS NOT NULL;

CREATE INDEX IF NOT EXISTS neighborhoods_name_native_trgm_idx
  ON public.neighborhoods USING gin (name_native gin_trgm_ops)
  WHERE name_native IS NOT NULL;

-- RLS
ALTER TABLE public.neighborhoods ENABLE ROW LEVEL SECURITY;

