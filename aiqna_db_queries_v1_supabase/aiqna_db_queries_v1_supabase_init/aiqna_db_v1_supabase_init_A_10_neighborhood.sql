-- TABLE: map_neighborhood (PK: (neighborhood_id, source_type, source_id))
--------------------------------------------------------------------------------
CREATE TABLE public.map_neighborhood (
  neighborhood_id BIGINT      NOT NULL,             -- ← 참조 ID
  source_type     VARCHAR(50) NOT NULL,
  source_id       VARCHAR(1023) NOT NULL,

  -- AI 추출 정보
  confidence_score   NUMERIC(3, 2) NULL,  -- 0.00 ~ 1.00
  extracted_at       TIMESTAMPTZ DEFAULT NOW(),
  extraction_method  VARCHAR(50) NULL,
  
  -- 관리 정보
  is_verified  BOOLEAN NOT NULL DEFAULT FALSE,
  verified_at  TIMESTAMPTZ NULL,
  verified_by  VARCHAR(511) NULL,
  
  is_selected  BOOLEAN NOT NULL DEFAULT FALSE,
  order_num    INTEGER NOT NULL DEFAULT 0,
  added_at     TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at   TIMESTAMPTZ DEFAULT NOW(),

  CONSTRAINT map_neighborhood_pkey
    PRIMARY KEY (neighborhood_id, source_type, source_id),

  CONSTRAINT map_neighborhood_neighborhood_id_fkey
    FOREIGN KEY (neighborhood_id) 
    REFERENCES public.neighborhoods (id)
    ON UPDATE CASCADE ON DELETE CASCADE,

  CONSTRAINT map_neighborhood_source_type_check 
    CHECK (source_type IN ('youtube_video', 'instagram_post', 'blog_post', 'text')),
  
  CONSTRAINT map_neighborhood_order_num_check CHECK (order_num >= 0),
  CONSTRAINT map_neighborhood_confidence_check 
    CHECK (confidence_score IS NULL OR (confidence_score BETWEEN 0 AND 1))
) TABLESPACE pg_default;

-- INDEXES
CREATE INDEX IF NOT EXISTS map_neighborhood_id_order_idx
  ON public.map_neighborhood (neighborhood_id, order_num, source_id);

CREATE INDEX IF NOT EXISTS map_neighborhood_id_added_idx
