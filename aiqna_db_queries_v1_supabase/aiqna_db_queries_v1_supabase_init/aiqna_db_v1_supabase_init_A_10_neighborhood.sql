--------------------------------------------------------------------------------
-- FUNCTION: upsert_neighborhood_mapping (ID 버전)
--------------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.upsert_neighborhood_mapping(
    p_neighborhood_id BIGINT,
    p_source_type VARCHAR(50),
    p_source_id   VARCHAR(1023),
    p_confidence_score NUMERIC(3, 2) DEFAULT NULL,
    p_extraction_method VARCHAR(50) DEFAULT NULL
)
RETURNS VOID
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
BEGIN
    INSERT INTO public.map_neighborhood (
        neighborhood_id,
        source_type,
        source_id,
        confidence_score,
        extraction_method,
        extracted_at
    ) VALUES (
        p_neighborhood_id,
        p_source_type,
        p_source_id,
        p_confidence_score,
        p_extraction_method,
        NOW()
    )
    ON CONFLICT (neighborhood_id, source_type, source_id) DO UPDATE SET
        confidence_score = EXCLUDED.confidence_score,
        extraction_method = EXCLUDED.extraction_method,
        extracted_at = NOW(),
        updated_at = NOW();
END;
$$;



--------------------------------------------------------------------------------
-- FUNCTION: get_neighborhoods_for_content (ID 반환)
--------------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.get_neighborhoods_for_content(
    p_source_type VARCHAR(50),
    p_source_id   VARCHAR(1023)
)
RETURNS TABLE (
    neighborhood_id BIGINT,
    name_en VARCHAR(100),
    name_ko VARCHAR(100),
    city_code VARCHAR(96),
    confidence_score NUMERIC(3, 2),
    is_verified BOOLEAN
)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
    SELECT 
        n.id AS neighborhood_id,
        n.name_en,
        n.name_ko,
        n.city_code,
        ms.confidence_score,
        ms.is_verified
    FROM public.map_neighborhood ms
    JOIN public.neighborhoods n ON ms.neighborhood_id = n.id
    WHERE ms.source_type = p_source_type
      AND ms.source_id = p_source_id
    ORDER BY ms.confidence_score DESC NULLS LAST, ms.order_num;
$$;



--------------------------------------------------------------------------------
-- FUNCTION: get_content_for_neighborhood (ID 입력)
--------------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.get_content_for_neighborhood(
    p_neighborhood_id BIGINT,
    p_source_type VARCHAR(50) DEFAULT NULL,
    p_verified_only BOOLEAN DEFAULT FALSE
)
RETURNS TABLE (
    source_type VARCHAR(50),
    source_id VARCHAR(1023),
    confidence_score NUMERIC(3, 2),
    is_verified BOOLEAN,
    added_at TIMESTAMPTZ
)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
    SELECT 
        ms.source_type,
        ms.source_id,
        ms.confidence_score,
        ms.is_verified,
        ms.added_at
    FROM public.map_neighborhood ms
    WHERE ms.neighborhood_id = p_neighborhood_id
      AND (p_source_type IS NULL OR ms.source_type = p_source_type)
      AND (NOT p_verified_only OR ms.is_verified = TRUE)
    ORDER BY ms.order_num, ms.added_at DESC;
$$;



--------------------------------------------------------------------------------
-- FUNCTION: get_neighborhoods_by_city (ID 반환)
--------------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.get_neighborhoods_by_city(
    p_city_code VARCHAR(96),
    p_active_only BOOLEAN DEFAULT TRUE
)
RETURNS TABLE (
    neighborhood_id BIGINT,
    name_en VARCHAR(100),
    name_ko VARCHAR(100),
    name_native VARCHAR(100),
    latitude NUMERIC(10, 8),
    longitude NUMERIC(11, 8),
    order_num SMALLINT
)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
    SELECT 
        n.id AS neighborhood_id,
        n.name_en,
        n.name_ko,
        n.name_native,
        n.latitude,
        n.longitude,
        n.order_num
    FROM public.neighborhoods n
    WHERE n.city_code = p_city_code
      AND (NOT p_active_only OR (n.is_active = TRUE AND n.is_display = TRUE))
    ORDER BY n.order_num, n.id;
$$;
